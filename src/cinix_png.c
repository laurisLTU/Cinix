void cinix_draw_image_argb(CinixApp* app, const CinixImage* image, int dst_x, int dst_y) {
    int x;
    int y;

    if (app == 0 || image == 0 || image->pixels == 0) {
        return;
    }

    for (y = 0; y < image->height; ++y) {
        for (x = 0; x < image->width; ++x) {
            uint32_t px = image->pixels[y * image->width + x];
            unsigned char a = (unsigned char)((px >> 24) & 0xFF);
            unsigned char r = (unsigned char)((px >> 16) & 0xFF);
            unsigned char g = (unsigned char)((px >> 8) & 0xFF);
            unsigned char b = (unsigned char)(px & 0xFF);
            if (a > 0) {
                XSetForeground(app->display, app->gc, cinix_rgb(app, r, g, b));
                XDrawPoint(app->display, app->surface, app->gc, dst_x + x, dst_y + y);
            }
        }
    }
}

